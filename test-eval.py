import json
import pandas as pd
import os
from datetime import datetime
from src.api.query_handler import query_handler
from src.services.llm_gateway import get_llm
from pydantic import BaseModel, Field


class AccuracyScore(BaseModel):
    """Model for storing and validating semantic accuracy scores.
    
    Attributes:
        score: A numeric score between 0 and 100 comparing RAG responses to expected
               responses based on semantic accuracy and factual correctness.
    """
    score: float = Field(
        ...,
        description="A pontuação de 0 a 100, comparando a Resposta RAG com a Resposta Esperada, com base na Query.",
        ge=0,
        le=100 
    )

def get_llm_comparison_score(query: str, received: str, expected: str) -> float:
    """Evaluate semantic accuracy of a RAG response against an expected response.
    
    Uses a structured LLM to compare a received RAG response with an expected
    reference response based on a user query. Evaluates factual accuracy and
    completeness on a scale of 0-100, following Portuguese European language criteria.
    
    Args:
        query: The original user query or question.
        received: The response generated by the RAG system.
        expected: The ideal or reference response for comparison.
    
    Returns:
        A float score between 0 and 100 representing semantic accuracy, where:
        - 95-100: Perfect response with all key information
        - 80-94: Good response with minor omissions
        - 50-79: Reasonable response with notable omissions
        - 10-49: Poor response with inaccuracies or hallucinations
        - 0: Completely incorrect or irrelevant response
    """
    llm = get_llm()
    llm = llm.bind(temperature=0)

    llm_with_schema = llm.with_structured_output(AccuracyScore)

    prompt_template = """
    ### Tarefa de Avaliação (Português Europeu)

    És um avaliador especialista de um sistema RAG (Retrieval-Augmented Generation).
    A tua tarefa é avaliar a 'Resposta RAG' com base na 'Query' do utilizador, usando a 'Resposta Esperada' como a fonte de "verdade" ou a resposta ideal.

    Compara a 'Resposta RAG' com a 'Resposta Esperada' com base nos seguintes critérios:
    1.  **Precisão Factual:** A 'Resposta RAG' contém informação factualmente correta, tal como definido pela 'Resposta Esperada'?
    2.  **Completude:** A 'Resposta RAG' cobre todos os pontos-chave e o significado central presentes na 'Resposta Esperada'?

    ### Critérios de Pontuação (0-100)

    * **95-100 (Perfeita):** A 'Resposta RAG' responde perfeitamente à 'Query' e é 100% precisa e completa. Contém toda a informação chave da 'Resposta Esperada'. Diferenças de estilo são aceitáveis.
    * **80-94 (Boa):** A 'Resposta RAG' é factualmente precisa e responde bem à 'Query', mas pode omitir pequenos detalhes ou ser ligeiramente menos completa que a 'Resposta Esperada'.
    * **50-79 (Razoável):** A 'Resposta RAG' responde à 'Query', mas é visivelmente incompleta ou omite factos importantes presentes na 'Resposta Esperada'.
    * **10-49 (Fraca):** A 'Resposta RAG' contém imprecisões factuais, "alucinações" (informação que contradiz a 'Resposta Esperada') ou é maioritariamente irrelevante para a 'Query'.
    * **0 (Terrível):** Completamente errada ou irrelevante.

    Foca-te apenas na precisão factual e na completude. Não penalizes por diferenças de estilo ou formulação, *desde que* o significado central seja o mesmo.

    ---
    ### Dados para Avaliar

    **Query:**
    "{query}"

    **Resposta Esperada (Ideal):**
    "{expected}"

    **Resposta RAG (Recebida):**
    "{received}"
    """
    
    response = llm_with_schema.invoke(
        prompt_template.format(
            query=query,
            expected=expected,
            received=received
        )
    )

    return response.score



def load_queries_from_json(json_file_path: str) -> list:
    """Load test queries and expected responses from a JSON file.
    
    Reads a JSON file containing test cases with query and expected response pairs.
    The JSON should be an array of objects with 'query' and 'expected' keys.
    
    Args:
        json_file_path: The path to the JSON file containing test queries.
    
    Returns:
        A list of dictionaries, each containing 'query' and 'expected' keys
        for use in evaluation tests.
    """
    with open(json_file_path, 'r', encoding='utf-8') as f:
        data = json.load(f)
    return data


    
def run_tests(queries: list) -> pd.DataFrame:
    """Execute RAG evaluation tests on a list of queries.
    
    Processes each query through the RAG system, compares the received response
    with the expected response, and generates semantic accuracy scores.
    Handles errors gracefully by recording error messages for failed queries.
    
    Args:
        queries: A list of dictionaries, each with 'query' and 'expected' keys.
    
    Returns:
        A pandas DataFrame with columns:
        - Query: The original query string
        - Received Response: The RAG system's response
        - Expected Response: The reference expected response
        - Meaning Acc (%): The semantic accuracy score (0-100)
    """
    results = []
    for item in queries:
        query = item['query']
        expected = item['expected']

        try:
            received = query_handler(query)["response"]
            received = received.replace('\n', ' ').replace('\r', ' ')
        except Exception as e:
            received = f"Error: {str(e)}"

        score = get_llm_comparison_score(
            query=query,
            received=received,
            expected=expected
        )

        results.append({
            'Query': query,
            'Received Response': received,
            'Expected Response': expected,
            'Meaning Acc (%)': score
        })
    return pd.DataFrame(results)

def main(json_file_path: str) -> None:
    """Execute the complete RAG evaluation pipeline and save results.
    
    Loads test queries from a JSON file, runs semantic accuracy evaluations
    on RAG responses, and saves the results as a timestamped CSV file in
    the outputs/results directory.
    
    Args:
        json_file_path: The path to the JSON file containing test queries
                        and expected responses.
    """
    queries = load_queries_from_json(json_file_path)
    results_df = run_tests(queries)
    
    os.makedirs('outputs', exist_ok=True)
    timestamp = datetime.now().strftime('%Y%m%d_%H%M%S')
    output_path = f'outputs/results/test_results_{timestamp}.csv'
    
    results_df.to_csv(output_path, index=False)
    print(f"Results saved to {output_path}")

if __name__ == "__main__":
    json_file = 'query.json'
    main(json_file)